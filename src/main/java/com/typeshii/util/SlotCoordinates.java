    // builds a SlotView for every slot  includes inline style for schematic lots, CSS class for grid fallback
    public static List<SlotView> buildSlotViews(List<Slot> slots, String lotName) {
        double[][] coords = null;
        if      ("Skill - Motorcycle".equals(lotName))  coords = SKILL_MOTO;
        else if ("Skill - Car".equals(lotName))         coords = SKILL_CAR;
        else if ("Himal - Motorcycle".equals(lotName))  coords = HIMAL_MOTO;
        else if ("Kumari - Motorcycle".equals(lotName)) coords = KUMARI_MOTO;
        else if ("Kumari - Car".equals(lotName))        coords = KUMARI_CAR;

        List<SlotView> views = new ArrayList<>();
        for (int i = 0; i < slots.size(); i++) {
            Slot slot = slots.get(i);
            SlotView sv = new SlotView();
            sv.setSlotNo(slot.getSlotNo());
            sv.setLotId(slot.getLotId());
            sv.setLabel(slot.getSlotLabel());

            // schematic lot build a positioned inline style; otherwise fall back to grid CSS class
            if (coords != null && i < coords.length) {
                double[] p = coords[i];
                String color = "Occupied".equals(slot.getSlotLabel()) ? "#dc2626"
                             : "Reserved".equals(slot.getSlotLabel()) ? "#ca8a04" : "#16a34a";
                sv.setStyle(String.format(
                    "position:absolute;left:%.4f%%;top:%.4f%%;width:%.4f%%;height:%.4f%%;display:flex;align-items:center;justify-content:center;font-size:10px;font-weight:600;font-family:sans-serif;border:1.5px solid %s;color:%s;background:transparent;border-radius:3px;text-decoration:none;box-sizing:border-box;cursor:pointer;",
                    p[0], p[1], p[2], p[3], color, color
                ));
            } else {
                String cssClass = "slot-box ";
                if      ("Occupied".equals(slot.getSlotLabel())) cssClass += "slot-occupied";
                else if ("Reserved".equals(slot.getSlotLabel())) cssClass += "slot-reserved";
                else                                              cssClass += "slot-available";
                sv.setCssClass(cssClass);
            }
            views.add(sv);
        }
        return views;
    }
